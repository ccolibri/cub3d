#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
		ft_putstr("-2147483648", 11);
	else if (n < 0)
	{
		ft_putchar('-');
		ft_putnbr_fd(-n, fd);
	}
	else if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putchar(n % 10 + '0');
	}
	else
		ft_putchar(n + '0');
}
