#include "libft.h"

char		*ft_strdup(const char *str)
{
	char	*new;
	int		i;

	i = 0;
	new = (char*)malloc((ft_strlen(str) + 1) * sizeof(char));
	if (new == NULL)
		return (NULL);
	while (str[i] != '\0')
	{
		new[i] = str[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
