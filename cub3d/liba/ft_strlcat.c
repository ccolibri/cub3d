#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t i;
	size_t j;
	size_t summ;

	i = 0;
	while (dst[i] != '\0' && i < size)
		i++;
	summ = ft_strlen(src) + i;
	j = 0;
	while (src[j] != '\0' && i + 1 < size)
	{
		dst[i] = src[j];
		i++;
		j++;
		dst[i] = '\0';
	}
	return (summ);
}
